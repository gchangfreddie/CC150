package Chapter1;

public class Question6 {
	public static void main(String[] args) {
		int i; 
		for (i=0; i<5; i++) {
			System.out.print(i + " ");
		}
		System.out.println( "& " + i);
		int k; 
		for (k=0; k<5;++k) {
			System.out.print(k + " ");
		}
		System.out.println("*" + k);
		
		System.exit(0);
	}
}
